function t(key) {
  return translations[state.language][key] || key;
}

function applyLanguage() {
  document.documentElement.lang = state.language === 'az' ? 'az' : 'en';
  document.querySelectorAll('[data-i18n]').forEach((node) => {
    const key = node.getAttribute('data-i18n');
    node.textContent = t(key);
  });
  els.problemLanguage.textContent = state.language.toUpperCase();
  els.ideaInput.placeholder = state.language === 'az'
    ? 'Öz yanaşmanı burada yaz...'
    : 'Write your own approach here...';
  els.langButtons.forEach((btn) => btn.classList.toggle('active', btn.dataset.langBtn === state.language));
  renderModeButtons();
  renderProblemCard();
  renderSources();
  renderActions();
  renderCheckpoints();
  renderFeed();
  renderSimilar();
  renderHistory();
  updateSpoilerLevel();
}

function applyTheme() {
  document.body.classList.toggle('light', state.theme === 'light');
}

function renderModeButtons() {
  els.modeButtons.forEach((btn) => btn.classList.toggle('active', btn.dataset.mode === state.mode));
  els.modePill.textContent = state.mode === 'guided' ? t('modeGuidedShort') : t('modeDirectShort');
}

function setStatus(key, tone = 'warning') {
  els.analysisStatus.textContent = t(key);
  const colorMap = {
    warning: 'var(--warning)',
    success: 'var(--success)',
    danger: 'var(--danger)'
  };
  const dot = document.querySelector('.status-dot');
  dot.style.background = colorMap[tone];
  dot.style.boxShadow = `0 0 0 6px color-mix(in srgb, ${colorMap[tone]} 16%, transparent)`;
}

function resetSession(problem) {
  state.sessionReady = true;
  state.currentProblem = problem;
  state.opened = [];
  state.unlocked = new Set(state.mode === 'direct'
    ? stepDefinitions.map((step) => step.id)
    : ['hint1']
  );
  els.codeBlock.textContent = '// The C++ solution appears here when the user unlocks it.';
  els.ideaFeedback.innerHTML = `<p>${t('feedbackPlaceholder')}</p>`;
  pushHistory(problem.link);
  renderProblemCard();
  renderSources();
  renderFeed(true);
  renderActions();
  renderCheckpoints();
  renderSimilar();
  updateStats();
}

function analyzeLink(link) {
  if (!link) {
    setStatus('analyzeFirst', 'danger');
    return;
  }
  setStatus('analyzing');
  const normalized = link.trim();
  const problem = {
    ...demoProblem,
    link: normalized,
    title: normalized.includes('/problemset/problem/') ? demoProblem.title : 'Custom Codeforces link',
  };
  window.setTimeout(() => {
    resetSession(problem);
    appendMessage('coach', t('feedSystemTitle'), t('feedSystemText'));
    setStatus('analyzed', 'success');
  }, 500);
}

function renderProblemCard() {
  if (!state.currentProblem) {
    els.problemTitle.textContent = '—';
    els.problemDifficulty.textContent = '—';
    els.problemContest.textContent = '—';
    els.problemTags.textContent = '—';
    els.problemGoal.textContent = '—';
    els.sourceConfidence.textContent = '—';
    return;
  }
  els.problemTitle.textContent = state.currentProblem.title;
  els.problemDifficulty.textContent = state.currentProblem.difficulty;
  els.problemContest.textContent = state.currentProblem.contest;
  els.problemTags.textContent = state.currentProblem.tags;
  els.problemGoal.textContent = t('goalText');
  els.sourceConfidence.textContent = state.currentProblem.sourceConfidence;
}

function renderSources() {
  const items = [
    { title: t('sourceOfficial'), confidence: 'high', desc: t('sourceOfficialDesc'), score: '1' },
    { title: t('sourceCommunity'), confidence: 'medium', desc: t('sourceCommunityDesc'), score: '2' },
    { title: t('sourceAi'), confidence: 'fallback', desc: t('sourceAiDesc'), score: '3' },
  ];

  els.sourceList.innerHTML = items.map((item) => `
    <article class="source-item">
      <div class="source-top">
        <h4>${item.title}</h4>
        <span class="source-confidence">#${item.score}</span>
      </div>
      <p>${item.desc}</p>
    </article>
  `).join('');
}

function renderFeed(clearOnly = false) {
  if (!state.sessionReady) {
    els.feed.innerHTML = `<div class="message coach-message placeholder"><p>${t('placeholderFeed')}</p></div>`;
    return;
  }
  if (clearOnly) {
    els.feed.innerHTML = '';
  }
  if (!state.opened.length && !els.feed.childElementCount) {
    appendMessage('coach', t('feedSystemTitle'), t('feedSystemText'));
  }
}

function appendMessage(type, title, content) {
  const wrapper = document.createElement('article');
  wrapper.className = `message ${type}-message`;
  wrapper.innerHTML = `
    <div class="message-title">
      <strong>${title}</strong>
      <span>${new Date().toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' })}</span>
    </div>
    <p>${content}</p>
  `;
  els.feed.appendChild(wrapper);
  els.feed.scrollTop = els.feed.scrollHeight;
}

function stepIsDone(id) {
  return state.opened.includes(id);
}

